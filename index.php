                <h2>História da Quandragular no Brasil</h2>
                <p style="text-align: justify;text-indent: 25px;" class="curiosidade-texto">Fundada em 1° de janeiro de 1923, com a inauguração de sua sede internacional Angelus Temple, em Los Angeles, Califórnia; a Igreja do Evangelho Quadrangular nasceu no coração de Deus e foi confiada às mãos da evangelista Aimée Semple McPherson, conhecida como "Irmã Aimee" que, em seu ministério, foi responsável por diversas ações que resultaram em grandes impactos evangelísticos.
                <br/></p>
                <p style="text-align: justify;text-indent: 25px;" class="curiosidade-texto">Nos primeiros meses de existência da sede internacional Angelus Temple, sete mil pessoas se converteram a Jesus e, trinta dias depois, foi inaugurado o Instituto de Treinamento Evangelístico e Missionário e uma sala de oração, tendo como base o versículo “orai sem cessar”.  Aimée dirigia 21 cultos por semana na sede Quadrangular com capacidade para cinco mil pessoas. Ela também participava de eventos públicos e parava completamente as ruas de Los Angeles, diretamente para o Angelus Temple.
                <br/></p>
                <p style="text-align: justify;text-indent: 25px;" class="curiosidade-texto">Em 1944, quando concluiu seu ministério, Aimée Semple McPherson passou a presidência do movimento Quadrangular e da Cruzada Internacional de Evangelização passou para seu filho Rolf K. McPherson, que serviu ao corpo diretivo por 44 anos. A mudança da liderança não desacelerou o progresso, pelo contrário, sob sua responsabilidade, o movimento passou de 400 igrejas para mais de 10 mil.</p>
